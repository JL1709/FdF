#include "../includes/fdf.h"

int	pr_error(char *s1, char *s2)
{
	if (s1)
	{
		ft_putstr("zsh: ");
		ft_putstr(s1);
		ft_putstr(": ");
	}
	if (s2)
		ft_putstr(s2);
	write(1, "\n", 1);
	return (1);
}
